import py5

def setup():
   import py5

def setup():
    py5.size(400, 400)               
    py5.background(255, 0, 0)        #background red
    py5.fill(255,255,0)              # circle colour yellow
    py5.circle(200, 200, 300)        # cicrle
    py5.fill(0, 255, 225)            # triangle colour blue
    py5.triangle(50, 350, 200, 50, 350, 350) #triangle formation
    py5.fill(180)  # grey color for ellipse
    py5.ellipse(200, 200, 180, 100)
    py5.fill(0) #black for pupil
    py5.circle(200, 200, 60) #second smaller circle

py5.run_sketch()

   
    
